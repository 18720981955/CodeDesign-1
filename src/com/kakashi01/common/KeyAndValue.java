package com.kakashi01.common;

public class KeyAndValue {

	private int	key;
	private int	value;

	public KeyAndValue(int key, int value) {
		super();
		this.key = key;
		this.value = value;
	}

	public int getKey() {
		return key;
	}

	public int getValue() {
		return value;
	}
}